function go(){
                 
      var value_a = document.getElementById('edit-value-a').value ; 
                    var value_b = document.getElementById('edit-value-b').value ; 
                    var operation = document.getElementById('edit-weight-units').value ; 
                    var result = 0 ; 
                    if(operation==='plus'){
                        result =  parseInt(value_a) +  parseInt(value_b) ; 
                    }else if(operation==='minus') {
                        result = parseInt(value_a) -  parseInt(value_b) ;  
                    }else if(operation==='product'){
                        result = parseInt(value_a) *  parseInt(value_b) ; 
                    }else if(operation==='division'){
                        result = parseInt(value_a) /  parseInt(value_b) ; 
                    }
            
                    $('#value_calculate_wrapper').html('<strong> Result :'+result+'</strong>' ) ; 

            }
            
            

